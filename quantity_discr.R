#' Type categoric data data to string
#'
#' @param vector Vector for calculation
#' @param parametr Which parameter
#' @param file_name Introductory explanation for forming relevant text
#'
#'
#' @return Print categoric data data to string
#'
#' @examples
#' parametr = "Engine is "
#' file_name <- "text.txt"
#' quantity_discr(parametr = parametr, vector = as.factor(mtcars$vs),file_name)


quantity_discr <- function(parametr, vector, file_name) {
  descr <- parametr
  a <- paste(descr, "\n")
  cat(a, file = file_name, append = TRUE)
  n <- length(data.frame(table(vector), row.names = TRUE)$Freq)
  i <- 0
  while (i < n){
    descr <- print(paste0(
                        data.frame(table(vector))[i+1,1],
                        " Ð² ",
                        data.frame(round(prop.table(table(vector))*100,2))[i+1,2],
                        "% (",
                        data.frame(table(vector))[i+1,2],
                        "/", length(vector),
                        ")", sep = ""))
    i <- i + 1
    a <- paste(descr,"\n")
    cat(a, file = file_name, append = TRUE)
  }
}
